export class CustomDrumOrders {
  drumColorOrder;
  drumMaterialOrder;
  drumDispositionOrder;
  drumStickTypeOrder;
  drumBrandOrder;


  constructor(drumColorOrder, drumMaterialOrder, drumDispositionOrder, drumStickTypeOrder, drumBrandOrder) {
    this.drumColorOrder = drumColorOrder;
    this.drumMaterialOrder = drumMaterialOrder;
    this.drumDispositionOrder = drumDispositionOrder;
    this.drumStickTypeOrder = drumStickTypeOrder;
    this.drumBrandOrder = drumBrandOrder;
  }
}
